CREATE TABLE IF NOT EXISTS `theme` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `name` VARCHAR(255) NOT NULL,
    `description` VARCHAR(255) NULL,
    `image_url` VARCHAR(255) NULL,
    `required_time` TIME NOT NULL,
    CONSTRAINT `PK_THEME` PRIMARY KEY (`id`)
);

CREATE TABLE IF NOT EXISTS `user` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `name` VARCHAR(255) NOT NULL,
    `role` VARCHAR(30) NOT NULL,
    CONSTRAINT `PK_USER` PRIMARY KEY (`id`)
);

CREATE TABLE IF NOT EXISTS `schedule` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `theme_id` BIGINT NOT NULL,
    `start_at` DATETIME NOT NULL,
    `end_at` DATETIME NOT NULL,
    CONSTRAINT `PK_SCHEDULE` PRIMARY KEY (`id`),
    CONSTRAINT `FK_SCHEDULE_THEME` FOREIGN KEY (`theme_id`) REFERENCES `THEME` (`id`),
    CONSTRAINT `UQ_SCHEDULE_THEME_ID_START_AT` UNIQUE (`theme_id`, `start_at`)
);

CREATE TABLE IF NOT EXISTS `reservation` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `user_id` BIGINT NOT NULL,
    `schedule_id` BIGINT NOT NULL,
    CONSTRAINT `PK_RESERVATION` PRIMARY KEY (`id`),
    CONSTRAINT `FK_RESERVATION_USER` FOREIGN KEY (`user_id`) REFERENCES `USER` (`id`),
    CONSTRAINT `FK_RESERVATION_SCHEDULE` FOREIGN KEY (`schedule_id`) REFERENCES `SCHEDULE` (`id`),
    CONSTRAINT `UQ_RESERVATION_SCHEDULE_ID` UNIQUE (`schedule_id`)
);
